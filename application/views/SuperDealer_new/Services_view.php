                                        var currentBal = jQuery('#currentMBal').html();
                                        var srvsPrice = jQuery('#hfPurchasePrice').val();
                                        if (parseFloat(currentBal) >= parseFloat(srvsPrice)) {
                                            jQuery('#modalConfrimation').modal('show');
                                            jQuery('#ActivationserviceList').val(srvs);
                                        } else {
                                            swal('', "You don't have sufficient balance for services activation.", 'error');
