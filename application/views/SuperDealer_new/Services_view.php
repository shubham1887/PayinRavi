                                    } else {
                                        swal('', 'Please select activation service.', 'error');
                                    }
                                });
                            });

                            function doPreProcessSrv(ID, price) {
                                if (jQuery(ID).is(":checked")) {
                                    var oldPrice = jQuery('#hfPurchasePrice').val();
                                    var totalAmt = parseFloat(oldPrice) + parseFloat(price);
                                    jQuery('#hfPurchasePrice').val(totalAmt);
                                    jQuery('#showPurchasePrice').html('<b style="font-size: 16px;">Purchase Price : </b><b style="color:#aa0000;font-size: 16px;">&#8377; ' + totalAmt + '</b>');
                                } else {
                                    var oldPrice = jQuery('#hfPurchasePrice').val();
                                    var totalAmt = parseFloat(oldPrice) - parseFloat(price);
                                    jQuery('#hfPurchasePrice').val(totalAmt);
                                    jQuery('#showPurchasePrice').html('<b style="font-size: 16px;">Purchase Price : </b><b style="color:#aa0000;font-size: 16px;">&#8377; ' + totalAmt + '</b>');
                                }
