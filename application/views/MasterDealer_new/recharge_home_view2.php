<script>
    function viewbillgas(optname, optvalue, mobileno, amount, optional1, optional2, optional3, optional4) {
        $(".loaderwww").show();
        $.ajax({
            type: "POST",
            url: "/Retailer/recharge_home/viewbill",
            data: { OperatorName: optname, OptCode: optvalue, mobileno: mobileno, Amount: amount, optional1: optional1, optional2: optional2, optional3: optional3, optional4: optional4 },
            cache: false,
            dataType: 'json',
            closeOnConfirm: true,
            success: function (result) {
                $('#gasCustomerTbl').empty();

                $(".loaderwww").hide();
                if (result.Response == 'SUCCESS') {
                                //Gas View Bill
              
                var price = result.Price;
                    var displayValues = "";
                    $.each(JSON.parse(result.DisplayValues), function (key, val) {
                        if (!val.label.includes('Incentive')) {

                            displayValues = displayValues + val.label + ' : ' + val.value + "</br>";
                        }
                    });
                if (price != "") {
                    //price = parseInt(price);
                    $('#gasamount').val(price);

                    var newrow = "<table class='col-sm-4 table-responsive table-bordered'><tr>";
                    newrow += "<td>" + displayValues + "</td></tr>";
                    newrow += "<tr><td> Bill Amount &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; ::  " + result.Price + "</td></tr>";
                    newrow += "</table>";
                    $('#gasCustomerTbl').append(newrow);
                    $('#gasCustomerInfo').css('display', 'block');
                    //swal("Success!", displayValues, "success")
                }
                else {
                                var message = result.Message;
                    $('#gasamount').val(0);

                    var newRow =
                        "<div class='col-sm-4' style='text-align: left;'>" +
                        "" + message + ""
                    "</div>"
                    $('#gasCustomerTbl').append(newRow);
                    $('#gasCustomerInfo').css('display', 'block');
                }
                }
                if (result.Response == 'ERROR') {
                
                   $('.loader').hide();
                                               var newRow ="<div class='col-sm-4' style='text-align: left;color:red'>" +
                                  ""+result.Message+""
                   "</div>";

                    $('#gasAmount').val('');
                     $('#gasCustomerTbl').append(newRow);
                              $('#gasCustomerInfo').css('display', 'block');
               }
            }
        })
    }

</script>


